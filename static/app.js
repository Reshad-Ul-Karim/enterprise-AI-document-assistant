/* Enterprise AI Document Assistant — UI.
 *
 * TWO MODES, deliberately:
 *   Demo   — the committed corpus (Employee Handbook + Labour Act). PUBLIC, no login. This
 *            is what a reviewer must land on: an assessment demo behind a password is a
 *            demo nobody sees.
 *   Notebook — upload your own PDFs and chat with them. Gated, because uploading consumes
 *            quota and writes to a store.
 *
 * Chat history lives HERE, in the client, and is sent with each request. A server-side
 * session store would die with the container on a free tier that sleeps and restarts.
 * Stateless server, history that survives every restart, zero storage.
 */

const $ = (id) => document.getElementById(id);
const esc = (s) => String(s).replace(/[&<>"]/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;" }[c]));

/* Minimal markdown renderer. The model answers in markdown; rendering it as plain text
 * showed literal asterisks. Hand-written rather than pulling marked.js from a CDN — that
 * would be a third-party request on every page load of a document-confidentiality demo.
 *
 * SECURITY: escape FIRST, then apply markdown to the escaped string. The reverse is how
 * renderers grow XSS holes, and this one renders text quoted from PDFs a user uploaded. */
function md(src) {
  let h = esc(src);
  h = h.replace(/^#{3,} (.*)$/gm, "<h4>$1</h4>").replace(/^#{1,2} (.*)$/gm, "<h3>$1</h3>");
  h = h.replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
       .replace(/(^|[^*])\*([^*\n]+)\*(?!\*)/g, "$1<em>$2</em>")
       .replace(/`([^`\n]+)`/g, "<code>$1</code>");
  h = h.replace(/(?:^[-*] .*(?:\n|$))+/gm, (m) =>
    "<ul>" + m.trim().split("\n").map((l) => `<li>${l.replace(/^[-*] /, "")}</li>`).join("") + "</ul>");
  h = h.replace(/(?:^\d+\. .*(?:\n|$))+/gm, (m) =>
    "<ol>" + m.trim().split("\n").map((l) => `<li>${l.replace(/^\d+\. /, "")}</li>`).join("") + "</ol>");
  return h.split(/\n{2,}/).map((b) => (/^\s*<(h[34]|ul|ol|pre)/.test(b) ? b : `<p>${b.replace(/\n/g, "<br>")}</p>`)).join("");
}

const DEMO_CHIPS = [
  { tag: "warm-up", q: "How many days of casual leave am I entitled to?" },
  { tag: "flagship", q: "Does our Employee Handbook comply with the Bangladesh Labour Act on maternity leave?" },
  { tag: "nuance", q: "We work Sun–Thu 9–5 and Sat 9:00–1:30. Is that legal?" },
  { tag: "gap", q: "How much overtime pay is required?" },
  { tag: "unknown", q: "What is the parental leave policy?" },
  { tag: "grounding", q: "Who is the Chairperson and where is the head office?" },
];

const S = {
  mode: "demo",          // demo | notebook
  auth: false,
  kbId: null,
  kbs: [],
  history: [],           // [{question, answer}] — client-held, sent with each request
  busy: false,
  uploadsPersist: false,
};

async function api(path, opts = {}) {
  const res = await fetch(path, { credentials: "same-origin", ...opts });
  const body = await res.json().catch(() => ({}));
  if (!res.ok) {
    const e = body.error || {};
    const err = new Error(e.message || `HTTP ${res.status}`);
    err.code = e.code;
    err.status = res.status;
    err.retryAfter = e.retry_after_s;
    throw err;
  }
  return body;
}

/* ---------------------------------------------------------------- rendering */

function setBusy(b) {
  S.busy = b;
  const go = $("go");
  if (go) { go.disabled = b; go.textContent = b ? "Thinking…" : "Ask"; }
  // SINGLE-FLIGHT: the free tier serves ~1 req/s and a reviewer clicks three chips in two
  // seconds. Disabling controls mid-flight means they physically cannot fire concurrently.
  document.querySelectorAll(".chip").forEach((c) => (c.disabled = b));
}

function renderCite(c) {
  const ref = c.section_no
    ? `${esc(c.doc_title)}, s.${c.section_no} ${esc(c.section_title || "")} — printed p.${c.printed_page} (PDF page ${c.pdf_page})`
    : `${esc(c.doc_title)} — page ${c.printed_page}${c.half ? ` (${c.half} half)` : ""}`;
  const modality = c.source_modality === "ocr"
    ? `<div class="modality">source: OCR of a scanned page — verify against the PDF</div>` : "";
  // The snippet is sliced from the source chunk by code, never generated by the model.
  return `<div class="cite"><div class="ref">${ref}</div><div class="snip">“${esc(c.snippet)}”</div>${modality}</div>`;
}

function renderTurn(q, r) {
  const refusal = r.insufficient_information;
  const head = refusal ? `<h3>Not found in the provided documents.</h3>` : "";
  const cites = r.citations.length
    ? `<div class="cites"><h4>${refusal ? "Closest related material" : "Sources"}</h4>${r.citations.map(renderCite).join("")}</div>`
    : "";
  return `
    <div class="turn">
      <div class="q">${esc(q)}</div>
      <div class="card${refusal ? " refusal" : ""}">
        <span class="route">${esc(r.route)}</span>
        ${head}<div class="md">${md(r.answer)}</div>${cites}
        <div class="meta">${r.latency_ms} ms · request ${esc(r.request_id)}</div>
      </div>
    </div>`;
}

function renderError(err) {
  const retry = err.retryAfter ? ` Retry in ${err.retryAfter}s.` : "";
  return `<div class="turn"><div class="card err-card">
    <strong class="err">${esc(err.code || err.status || "Error")}</strong>
    <div>${esc(err.message)}${retry}</div></div></div>`;
}

/* ------------------------------------------------------------------- asking */

async function ask(question) {
  if (!question || S.busy) return;
  if (S.mode === "notebook" && !S.kbId) { alert("Create or pick a notebook first."); return; }

  setBusy(true);
  // LOADING ANIMATION on every response, both modes. The model call is 3-15s on a 0.1-CPU
  // free tier; a frozen page for that long reads as broken. The stage text is honest about
  // where the time actually goes -- retrieval is milliseconds, the model is seconds -- so
  // it is not a fake progress bar, it is the real pipeline narrated.
  $("thread").insertAdjacentHTML("beforeend",
    `<div class="turn pending"><div class="q">${esc(question)}</div>
     <div class="card">
       <div class="thinking"><span class="dots"><i></i><i></i><i></i></span><span>Thinking…</span></div>
       <div class="stage" id="stage"></div>
     </div></div>`);
  $("thread").scrollTop = $("thread").scrollHeight;

  const STAGES = S.mode === "notebook"
    ? ["Embedding your question…", "Searching your documents…", "Reading the passages…", "Writing the answer…", "Verifying every quote against the source…"]
    : ["Embedding your question…", "Searching the Labour Act…", "Reading the handbook…", "Writing the answer…", "Verifying every quote against the source…"];
  let si = 0;
  const stageEl = $("stage");
  if (stageEl) stageEl.textContent = STAGES[0];
  const ticker = setInterval(() => {
    si = Math.min(si + 1, STAGES.length - 1);
    if (stageEl) stageEl.textContent = STAGES[si];
  }, 1600);

  try {
    const r = await api("/api/ask", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        question,
        kb_id: S.mode === "notebook" ? S.kbId : "default",
        history: S.history.slice(-5),   // last 5 turns is plenty; the window is 262k tokens
      }),
    });
    document.querySelector(".turn.pending")?.remove();
    $("thread").insertAdjacentHTML("beforeend", renderTurn(question, r));
    S.history.push({ question, answer: r.answer.slice(0, 8000) });
  } catch (err) {
    document.querySelector(".turn.pending")?.remove();
    $("thread").insertAdjacentHTML("beforeend", renderError(err));
    if (err.status === 401) { S.auth = false; render(); }
  } finally {
    clearInterval(ticker);
    setBusy(false);
    $("q").value = "";
    $("thread").scrollTop = $("thread").scrollHeight;
  }
}

/* ------------------------------------------------------------------ uploads */

async function pollJob(jobId, kbId) {
  const box = $("upload-status");
  // Poll rather than block: a 60-page scanned PDF is an OCR round-trip plus embedding --
  // minutes, not seconds. A synchronous upload would hit the proxy timeout and show a 502
  // while the work was still succeeding.
  for (let i = 0; i < 200; i++) {
    let j;
    try { j = await api(`/api/jobs/${jobId}`); }
    catch (err) { box.innerHTML = `<span class="err">${esc(err.message)}</span>`; return; }

    if (j.state === "failed") {
      box.innerHTML = `<span class="err">Upload failed: ${esc(j.error || "unknown error")}</span>`;
      return;
    }
    if (j.state === "done") {
      const how = j.modality === "ocr"
        ? "scanned → OCR'd via Mistral (no local compute)"
        : "text-native → extracted locally, no API call";
      box.innerHTML = `<span class="ok">✓ ${esc(j.filename)} — ${j.pages} pages, ${j.chunks} chunks (${how})</span>`;
      await loadKbs();
      $("file").value = "";
      return;
    }
    box.innerHTML = `<span class="spin"></span> ${esc(j.progress || j.state)}…`;
    await new Promise((r) => setTimeout(r, 1500));
  }
  box.innerHTML = `<span class="err">Timed out waiting for ingestion.</span>`;
}

async function upload() {
  const file = $("file").files[0];
  if (!file) return;
  if (!S.kbId) { alert("Create or pick a notebook first."); return; }

  const fd = new FormData();
  fd.append("file", file);
  $("upload-status").innerHTML = `<span class="spin"></span> Uploading ${esc(file.name)}…`;
  try {
    const { job_id } = await api(`/api/kb/${S.kbId}/documents`, { method: "POST", body: fd });
    await pollJob(job_id, S.kbId);
  } catch (err) {
    $("upload-status").innerHTML = `<span class="err">${esc(err.code || "")} ${esc(err.message)}</span>`;
  }
}

async function loadKbs() {
  try {
    const { knowledge_bases, uploads_persist } = await api("/api/kb");
    S.kbs = knowledge_bases;
    S.uploadsPersist = uploads_persist;
    if (S.kbId && !S.kbs.find((k) => k.kb_id === S.kbId)) S.kbId = null;
    if (!S.kbId && S.kbs.length) S.kbId = S.kbs[0].kb_id;
    renderNotebook();
  } catch { /* not signed in */ }
}

async function createKb() {
  const name = $("kb-name").value.trim();
  if (!name) return;
  try {
    const kb = await api("/api/kb", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ name }),
    });
    $("kb-name").value = "";
    S.kbId = kb.kb_id;
    S.history = [];
    $("thread").innerHTML = "";
    await loadKbs();
  } catch (err) { alert(`${err.code || ""} ${err.message}`); }
}

async function deleteKb(kbId) {
  if (!confirm("Delete this notebook and everything in it?")) return;
  try {
    await api(`/api/kb/${kbId}`, { method: "DELETE" });
    if (S.kbId === kbId) { S.kbId = null; S.history = []; $("thread").innerHTML = ""; }
    await loadKbs();
  } catch (err) { alert(err.message); }
}

/* --------------------------------------------------------------------- auth */

async function login(e) {
  e.preventDefault();
  const btn = $("login-btn");
  btn.disabled = true; btn.textContent = "Signing in…";
  try {
    await api("/api/auth/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ email: $("email").value, password: $("password").value }),
    });
    $("login-err").textContent = "";
    S.auth = true;
    await loadKbs();
    render();
  } catch (err) {
    $("login-err").textContent = err.message;
  } finally {
    btn.disabled = false; btn.textContent = "Sign in";
  }
}

async function logout() {
  await api("/api/auth/logout", { method: "POST" }).catch(() => {});
  S.auth = false; S.kbId = null; S.kbs = []; S.history = [];
  $("thread").innerHTML = "";
  S.mode = "demo";
  render();
}

function newSession() {
  // "Close this session and start a new one": clears the conversation, keeps the notebook.
  // Cheap because history is client-side -- there is no server state to reset.
  S.history = [];
  $("thread").innerHTML = "";
  $("q").value = "";
  $("q").focus();
}

/* ------------------------------------------------------------------ chrome */

function renderNotebook() {
  const wrap = $("notebook-panel");
  if (!wrap) return;
  const persist = S.uploadsPersist
    ? `<span class="ok-badge">Persistent</span> Uploads are stored in Pinecone and survive restarts.`
    : `<span class="warn-badge">Ephemeral</span> No <code>PINECONE_API_KEY</code> on this deployment, so uploaded notebooks live in memory and <strong>are lost when the server restarts or sleeps</strong>. Re-upload to restore — ingestion is idempotent, so it never duplicates.`;

  wrap.innerHTML = `
    <div class="persist-note">${persist}</div>
    <div class="kb-row">
      <input id="kb-name" type="text" placeholder="New notebook name…" maxlength="80">
      <button id="kb-create" type="button">Create</button>
    </div>
    ${S.kbs.length ? `<div class="kb-list">${S.kbs.map((k) => `
      <div class="kb ${k.kb_id === S.kbId ? "active" : ""}" data-kb="${esc(k.kb_id)}">
        <div>
          <div class="kb-name">${esc(k.name)}</div>
          <div class="kb-meta">${k.documents} doc${k.documents === 1 ? "" : "s"} · ${k.chunks} chunks
            ${k.doc_titles.length ? `· ${k.doc_titles.map(esc).join(", ")}` : ""}</div>
        </div>
        <button class="kb-del" data-del="${esc(k.kb_id)}" title="Delete">×</button>
      </div>`).join("")}</div>` : `<p class="hint">No notebooks yet. Create one, then upload a PDF.</p>`}
    ${S.kbId ? `
      <div class="upload-row">
        <input type="file" id="file" accept="application/pdf">
        <button id="upload-btn" type="button">Upload PDF</button>
      </div>
      <div id="upload-status" class="upload-status"></div>
      <p class="hint">Scanned PDFs are OCR'd via Mistral — no local compute, so it works on a
        free-tier box. Limits: 20 MB, 60 pages.</p>` : ""}`;

  $("kb-create")?.addEventListener("click", createKb);
  $("upload-btn")?.addEventListener("click", upload);
  wrap.querySelectorAll(".kb").forEach((el) =>
    el.addEventListener("click", (e) => {
      if (e.target.dataset.del) return;
      S.kbId = el.dataset.kb; S.history = []; $("thread").innerHTML = ""; renderNotebook();
    }));
  wrap.querySelectorAll(".kb-del").forEach((el) =>
    el.addEventListener("click", (e) => { e.stopPropagation(); deleteKb(el.dataset.del); }));
}

function render() {
  $("tab-demo").classList.toggle("on", S.mode === "demo");
  $("tab-notebook").classList.toggle("on", S.mode === "notebook");
  $("demo-panel").style.display = S.mode === "demo" ? "" : "none";
  $("notebook-side").style.display = S.mode === "notebook" ? "" : "none";
  $("login-panel").style.display = S.mode === "notebook" && !S.auth ? "" : "none";
  $("composer").style.display = S.mode === "demo" || S.auth ? "" : "none";
  $("session-bar").style.display = S.auth ? "" : "none";
  $("q").placeholder = S.mode === "demo"
    ? "Ask about company policy or the Labour Act…"
    : "Ask about your uploaded documents…";
  if (S.mode === "notebook" && S.auth) renderNotebook();
}

function switchMode(mode) {
  S.mode = mode;
  S.history = [];
  $("thread").innerHTML = "";
  render();
}

/* --------------------------------------------------------------------- init */

(async function init() {
  $("chips").innerHTML = DEMO_CHIPS.map((c) =>
    `<button class="chip" type="button" data-q="${esc(c.q)}"><span class="tag">${c.tag}</span> · ${esc(c.q)}</button>`).join("");
  document.querySelectorAll(".chip").forEach((b) =>
    b.addEventListener("click", () => ask(b.dataset.q)));

  $("composer").addEventListener("submit", (e) => { e.preventDefault(); ask($("q").value.trim()); });
  $("tab-demo").addEventListener("click", () => switchMode("demo"));
  $("tab-notebook").addEventListener("click", () => switchMode("notebook"));
  $("login-form").addEventListener("submit", login);
  $("logout").addEventListener("click", logout);
  $("new-session").addEventListener("click", newSession);

  try {
    const me = await api("/api/auth/me");
    S.auth = me.authenticated;
    S.uploadsPersist = me.uploads_persist;
    if (S.auth) await loadKbs();
  } catch { /* ignore */ }
  render();
})();
